public class VendorModel {
    private int id;
    private String vendor_id;
    private String vendor_name;
    private String vendor_phone;
    private String  vendor_email;
    private String vendor_contact;
    private String vendor_bank;
    private String vendor_acctnum;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getVendor_id() {
        return vendor_id;
    }

    public void setVendor_id(String vendor_id) {
        this.vendor_id = vendor_id;
    }

    public String getVendor_name() {
        return vendor_name;
    }

    public void setVendor_name(String vendor_name) {
        this.vendor_name = vendor_name;
    }

    public String getVendor_phone() {
        return vendor_phone;
    }

    public void setVendor_phone(String vendor_phone) {
        this.vendor_phone = vendor_phone;
    }

    public String getVendor_email() {
        return vendor_email;
    }

    public void setVendor_email(String vendor_email) {
        this.vendor_email = vendor_email;
    }

    public String getVendor_contact() {
        return vendor_contact;
    }

    public void setVendor_contact(String vendor_contact) {
        this.vendor_contact = vendor_contact;
    }

    public String getVendor_bank() {
        return vendor_bank;
    }

    public void setVendor_bank(String vendor_bank) {
        this.vendor_bank = vendor_bank;
    }

    public String getVendor_acctnum() {
        return vendor_acctnum;
    }

    public void setVendor_acctnum(String vendor_acctnum) {
        this.vendor_acctnum = vendor_acctnum;
    }
    
    
    
}
